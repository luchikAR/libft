#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int n2;

	n2 = n;
	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n2 = n * -1;
	}
	if (n2 > 9)
		ft_putnbr_fd(n2 / 10, fd);
	ft_putchar_fd((n2 % 10) + '0', fd);
}
